local scene = Scenario.new("orc_dredge_group_attack")

-- Model the orc raiders making a group attack.
scene:campaign{
  name = "Orc Raider Group Attack",
  system = "DAGGERHEART",
  gm_mode = "HUMAN",
  theme = "battle"
}

scene:pc("Aragorn")
scene:adversary("Orc Raiders")

-- The GM spends fear to have the raiders strike as a group.
scene:start_session("Group Attack")
scene:gm_fear(1)

-- Example: single group attack roll against Aragorn's Evasion.
-- Missing DSL: represent group attack roll and shared damage.
scene:gm_spend_fear(1):spotlight("Orc Raiders")
scene:adversary_attack{
  actor = "Orc Raiders",
  target = "Aragorn",
  difficulty = 0,
  damage_type = "physical"
}

-- Close the session after the group strike.
scene:end_session()

return scene
